package adv_prog_ass2;

public class Customer {
    private String cname, caddress, cemail;

    private int cid;

    public int getCid() {
        return cid;
    }

    public void setCid(int cid) {
        this.cid = cid;
    }

    public String getCname() {
        return cname;
    }

    public void setCname(String cname) {
        this.cname = cname;
    }

    public String getCaddress() {
        return caddress;
    }

    public void setCaddress(String caddress) {
        this.caddress = caddress;
    }

    public String getCemail() {
        return cemail;
    }

    public void setCemail(String cemail) {
        this.cemail = cemail;
    }

    public Customer(int cid, String cname, String caddress, String cemail) {
        this.cid = cid;
        this.cname = cname;
        this.caddress = caddress;
        this.cemail = cemail;
    }
  
}
